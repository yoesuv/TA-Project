package proses;

public class Inputan {

	private String input,template;
	private int X,Y;
	private double batas;
	
	public String getInput(){
		return input;
	}
	
	public void setInput(String i){
		this.input = i;
	}
	
	public String getTemplate(){
		return template;
	}
	
	public void setTemplate(String t){
		this.template = t;
	}
	
	public int getX(){
		return X;
	}
	
	public void setX(int x){
		this.X = x;
	}
	
	public int getY(){
		return Y;
	}
	
	public void setY(int y){
		this.Y = y;
	}
	
	public double getBatas(){
		return batas;
	}
	
	public void setBatas(double b){
		this.batas = b;
	}
}
